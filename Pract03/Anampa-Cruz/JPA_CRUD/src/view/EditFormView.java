	private void initForm() throws Exception {
		if (this.id == null) {
			this.setTitle("NUEVO ALUMNO");
			cleanForm();
		} else {
			this.setTitle("EDITAR ALUMNO " + this.id);
			loadAlumno();
		}
	}

	private void cleanForm() {
		this.txtLocal.setText("");
		this.txtIncidencia.setText("");
		this.txtGravedad.setText("");
		this.txtLocal.grabFocus();
	}

	private void loadAlumno() throws Exception {
		Reporte alu = dao.read(this.id);
		this.txtLocal.setText(alu.getLocal());
		this.txtIncidencia.setText(alu.getTipo());
		this.txtGravedad.setText(alu.getGravedad());
                this.txtDescripcion.setText(alu.getDescripcion());
                this.txtFecha.setText(alu.getFecha());
                this.txtHora.setText(alu.getHora());
                this.txtRecomendacion.setText(alu.getRecomendacion());
		this.txtLocal.grabFocus();
	}
