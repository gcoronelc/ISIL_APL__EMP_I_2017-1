	 private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
		 try {
			 Reporte alu = new Reporte();
			 alu.setLocal(this.txtLocal.getText());
			 alu.setTipo(this.txtIncidencia.getText());
                         alu.setGravedad(this.txtGravedad.getText());
			 alu.setDescripcion(this.txtDescripcion.getText());
                         alu.setFecha(this.txtFecha.getText());
			 alu.setHora(this.txtHora.getText());
                         alu.setRecomendacion(this.txtRecomendacion.getText());
			 //alu.setGravedad(Integer.parseInt(this.txtGravedad.getText()));
			 if (this.id == null) {
				 dao.create(alu);
			 } else {
				 alu.setId(id);
				 dao.update(alu);
			 }
			 util.Util.showMessage(this, "Proceso ejecutado correctamente.");
			 if( this.id == null ){
				 cleanForm();
			 }
		 } catch (Exception e) {
			 util.Util.showError(this, e.getMessage());
		 }
	 }//GEN-LAST:event_btnGrabarActionPerformed

