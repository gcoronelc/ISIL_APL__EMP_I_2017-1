	private void initForm() throws Exception {
		if (this.id == null) {
			this.setTitle("NUEVO ARTICULO");
			cleanForm();
		} else {
			this.setTitle("EDITAR ARTICULO " + this.id);
			loadArticulo();
		}
	}

	private void cleanForm() {
		this.txtCosto.setText("");
		this.txtVenta.setText("");
		this.txtNombre.setText("");
		this.txtStock.setText("");
		this.txtNombre.grabFocus();
	}

	private void loadArticulo() throws Exception {
		Articulo art = dao.read(this.id);
		this.txtNombre.setText(art.getNombre());
		this.txtStock.setText(String.valueOf(art.getStock()));
		this.txtCosto.setText(String.valueOf(art.getPrecosto()));
		this.txtVenta.setText(String.valueOf(art.getPreventa()));
		this.txtNombre.grabFocus();
	}
