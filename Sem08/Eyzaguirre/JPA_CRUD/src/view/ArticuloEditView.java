	 private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
		 try {
			 Articulo art = new Articulo();
			 art.setNombre(this.txtNombre.getText());
			 art.setPrecosto(Double.parseDouble(this.txtCosto.getText()));
			 art.setPreventa(Double.parseDouble(this.txtVenta.getText()));
			 art.setStock(Double.parseDouble(this.txtStock.getText()));
			 if (this.id == null) {
				 dao.create(art);
			 } else {
				 art.setId(id);
				 dao.update(art);
			 }
			 util.Util.showMessage(this, "Proceso ejecutado correctamente.");
			 if( this.id == null ){
				 cleanForm();
			 } else{
                             this.dispose();
                         }
		 } catch (Exception e) {
			 util.Util.showError(this, e.getMessage());
		 }
	 }//GEN-LAST:event_btnGrabarActionPerformed

